//Java is a Case sensitive language.
public class day3 {

 		public static void main(String args[])
	{
		byte a = 10;
		System.out.println("a = " +a);
		
		a = 014; 					  //Octal
		System.out.println("a = " +a);

		a = 0xA; 					//hexadecimal
		System.out.println("a = " +a);

		a = 0xa; 					//hexadecimal
		System.out.println("a = " +a);

	}
}